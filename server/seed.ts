function p(o: Partial<SeedPepper> & { cultivar: string; plant_code: string }): SeedPepper {
  return {
    cultivar: o.cultivar,
    plant_code: o.plant_code,
    species: o.species ?? "chinense",
    heat_level: o.heat_level ?? 3,
    flavor_tags: o.flavor_tags ?? [],
    flavor_subtags: o.flavor_subtags ?? [],
    color_tags: o.color_tags ?? ["red"],
    age: o.age ?? "seedling",
    status: o.status ?? "Ready to transplant",
    current_pot: o.current_pot ?? "Cups / unassigned",
    recommended_pot: o.recommended_pot ?? tenGalFabric,
    ideal_pot: o.ideal_pot ?? tenGalFabric,
    pot_material: o.pot_material ?? "cup",
    location: o.location ?? "Rooftop",
    thumbnail: o.thumbnail ?? "🌶️",
    description: o.description ?? "",
    special: o.special ?? "",
    notes: o.notes ?? "",
    priority: o.priority ?? "normal",
    use_cases: o.use_cases ?? [],
    fixed: o.fixed ?? false,
    seeded: true,
  };
}

