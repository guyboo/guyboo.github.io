function updatePlant(cultivar: string, plant_code: string, updates: Partial<SeedPepper>) {
  const plant = SEED_PEPPERS.find((x) => x.cultivar === cultivar && x.plant_code === plant_code);
  if (!plant) throw new Error(`Missing seed plant: ${cultivar} ${plant_code}`);
  Object.assign(plant, updates);
}

function assignPot(cultivar: string, plant_code: string, pot: string, material = "fabric (light)", note?: string) {
  updatePlant(cultivar, plant_code, {
    recommended_pot: pot,
    ideal_pot: pot === fiveGal ? sevenGalFabric : tenGalFabric,
    pot_material: material,
    notes: note ?? `${plant_code} planned for ${pot}.`,
  });
}

